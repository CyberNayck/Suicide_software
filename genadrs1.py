import random

# Списки городов, улиц и других элементов
cities = [
    "Киев", "Львов", "Одесса", "Харьков", "Днепр",
    "Запорожье", "Херсон", "Чернигов", "Сумы", "Винница"
]

streets = [
    "Шевченка", "Леси Украинки", "Кобзаря", "Грушевского", "Мазепы",
    "Гагарина", "Свободы", "Кирова", "Независимости", "Ленина"
]

regions = [
    "Киевская область", "Львовская область", "Одесская область", "Харьковская область",
    "Днепропетровская область", "Запорожская область", "Херсонская область", 
    "Черниговская область", "Сумская область", "Винницкая область"
]

def generate_random_address():
    """Генерирует случайный украинский адрес."""
    city = random.choice(cities)
    street = random.choice(streets)
    house_number = random.randint(1, 100)
    apartment_number = random.randint(1, 100)
    region = random.choice(regions)
    
    return f"{city}, вул. {street}, буд. {house_number}, кв. {apartment_number}, Область: {region}"

# Пример использования
if __name__ == "__main__":
    print("Генерация случайных украинских адресов:")
    for _ in range(1000):  # Генерируем 10 адресов
        print(generate_random_address())
