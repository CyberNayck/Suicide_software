import random

# Список цитат
quotes = [
    "Не бойтесь совершенства — вам его не достичь. - Сальвадор Дали",
    "Будьте собой, все остальные роли уже заняты. - Оскар Уайльд",
    "Кто хочет, тот ищет возможности, кто не хочет, тот ищет причины. - Луций Сенека",
    "Секрет изменений заключается в том, чтобы сосредоточить всю свою энергию, а не на борьбе со старым, а на строительстве нового. - Сократ",
    "Чтобы быть незаменимым, нужно всегда быть различным. - Коко Шанель",
    "Успех — это не окончательная остановка, неудача — не фатальна: важно мужество продолжать. - Уинстон Черчилль",
    "Жизнь состоит из выборов. Делайте правильные. - Лао Цзы",
    "Сложности подготовили меня к великолепию. - Ральф Уолдо Эмерсон",
    "Верьте в себя, так же, как в Бога. - Поль Клуэль",
    "Вы никогда не сможете пересечь океан, если не осмелитесь потерять берег из вида. - Христофор Колумб"
]

def generate_quotes(num_quotes):
    return random.sample(quotes, min(num_quotes, len(quotes)))  # Получаем уникальные цитаты

while True:
    num = int(input("Введите количество генерируемых цитат (0 для выхода): "))
    
    if num == 0:
        print("Выход из программы.")
        break

    if num < 0:
        print("Введите положительное число или 0 для выхода.")
        continue
    
    generated_quotes = generate_quotes(num)

    # Выводим сгенерированные цитаты
    for index, quote in enumerate(generated_quotes):
        print(f"Цитата {index + 1}: {quote}")
