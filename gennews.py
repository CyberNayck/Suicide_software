def generate_news(n=1):
    news = []
    for _ in range(n):
        headline = random.choice(headlines)
        body = random.choice(news_bodies)
        news.append(f"{headline}\n{body}\n")
    return news

if __name__ == "__main__":
    while True:
        user_input = input("Введите количество новостей, которые вы хотите получить (или '0' для выхода): ")
        if user_input.isdigit() and int(user_input) >= 0:
            n = int(user_input)
            if n == 0:
                print("Выход из программы. Хорошего дня!")
                break
            # Генерация и вывод запрашиваемого количества новостей
            generated_news = generate_news(n)
            for news_item in generated_news:
                print(news_item)
        else:
            print("Пожалуйста, введите неотрицательное число.")
