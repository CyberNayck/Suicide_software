import random

# Список обычных шуток
jokes = [
    "Почему нельзя доверять лестницам? Потому что они всегда поднимают вас!",
    "Какой музыкальный инструмент можно взять в туалет? Уборная!",
    "Почему компьютер никогда не чувствует себя одиноким? Потому что у него есть много байтов!",
    "Почему коты всегда сидят на компьютере? Потому что они хотят мышку!",
    "Какой улитки скажет, когда доедет до финиша? Ура, я на 'остановке'!",
    "Почему никто не верит тому, кто стал мукой? Потому что он всегда в рассыпухе!",
    "Что говорит один океан другому? 'Ничего, они просто машут!'"
]

def joke_generator():
    print("Генератор шуток")

    while True:
        num_jokes = input("\nСколько шуток вы хотите сгенерировать? (Введите 0 для выхода): ")

        if num_jokes == '0':
            print("Выход из программы.")
            break

        try:
            num_jokes = int(num_jokes)
            if num_jokes < 0:
                print("Пожалуйста, введите положительное число или 0 для выхода.")
                continue

            print("\nСгенерированные шутки:")
            for _ in range(min(num_jokes, len(jokes))):  # Ограничиваем количество шуток
                print(random.choice(jokes))
        
        except ValueError:
            print("Некорректный ввод. Пожалуйста, введите число.")

# Запуск генератора
if __name__ == "__main__":
    joke_generator()
