def generate_story():
    setting = random.choice(settings)
    character = random.choice(characters)
    plot = random.choice(plots)
    twist = random.choice(twists)
    return f"Ты находишься {setting}, и вдруг появляется {character} {plot} {twist}"

def story_generator():
    print("Генератор страшных историй")

    while True:
        num_stories = input("\nСколько страшных историй вы хотите сгенерировать? (Введите 0 для выхода): ")

        if num_stories == '0':
            print("Выход из программы.")
            break

        try:
            num_stories = int(num_stories)
            if num_stories < 0:
                print("Пожалуйста, введите положительное число или 0 для выхода.")
                continue

            print("\nСгенерированные страшные истории:")
            for _ in range(num_stories):
                print(generate_story())
        
        except ValueError:
            print("Некорректный ввод. Пожалуйста, введите число.")

# Запуск генератора
if __name__ == '__main__':
    story_generator()
