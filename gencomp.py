def generate_company_names(number):
    """Генерирует заданное количество названий компаний."""
    generated_names = []
    for _ in range(number):
        adjective = random.choice(adjectives)
        noun = random.choice(nouns)
        generated_names.append(f"{adjective} {noun}")
    return generated_names

def main():
    while True:
        try:
            count = int(input("Сколько названий компаний сгенерировать? (введите число, 0 для выхода) "))
            if count == 0:
                print("Выход из программы.")
                break
            elif count < 0:
                print("Пожалуйста, введите положительное число или 0 для выхода.")
                continue
            
            result = generate_company_names(count)
            print("\nСгенерированные названия компаний:")
            for name in result:
                print(name)
            print()  # Просто для добавления пустой строки после списка
        except ValueError:
            print("Пожалуйста, введите корректное число.")

if __name__ == "__main__":
    main()
