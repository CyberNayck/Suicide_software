import random

# Расширенный список прилагательных
adjectives = [
    "Солнечная", "Зеленая", "Живописная", "Уютная",
    "Мирная", "Сказочная", "Спокойная", "Тихая",
    "Историческая", "Красивая", "Модернистская", "Древняя",
    "Очаровательная", "Вечная", "Шумная", "Добрая",
    "Жизнерадостная", "Заброшенная", "Тропическая", "Северная",
    "Веселая", "Уникальная", "Тенистая", "Радужная",
    "Незабываемая", "Лесная", "Степная", "Кристальная",
]

# Расширенный список существительных
nouns = [
    "Аллея", "Улица", "Проспект", "Сквер",
    "Переулок", "Квартал", "Бульвар", "Набережная",
    "Площадь", "Шоссе", "Дорога", "Канал",
    "Парк", "Участок", "Тропа", "Спуск",
    "Променад", "Залив", "Перекресток", "Насыпь",
    "Сад", "Станция", "Круг", "Пристань",
    "Сторона", "Территория", "Поток", "Вдоль"
]

def generate_street_names(number):
    """Генерирует заданное количество названий улиц."""
    generated_names = []
    for _ in range(number):
        adjective = random.choice(adjectives)
        noun = random.choice(nouns)
        generated_names.append(f"{adjective} {noun}")
    return generated_names

def main():
    while True:
        try:
            count = int(input("Сколько названий улиц сгенерировать? (введите число, 0 для выхода) "))
            if count == 0:
                print("Выход из программы.")
                break
            elif count < 0:
                print("Пожалуйста, введите положительное число или 0 для выхода.")
                continue
            
            result = generate_street_names(count)
            print("\nСгенерированные названия улиц:")
            for name in result:
                print(name)
            print()  # Просто для добавления пустой строки после списка
        except ValueError:
            print("Пожалуйста, введите корректное число.")

if __name__ == "__main__":
    main()
