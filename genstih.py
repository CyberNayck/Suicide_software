import random

# Список строк для стихов
lines = [
    "Светит луна, сверкает звезда,",
    "В лесу расцветают цветы весной.",
    "Морской прибой поет свою весну,",
    "Сквозь облака летит мечта.",
    "Листья шепчут с ветром в саду,",
    "Под небом голубым, счастье найду.",
    "Как птицы свободны в небесах,",
    "Здесь каждый миг как жизнь — искра.",
    "Течет река, унося мечты,",
    "Забуду я о горестях и бедах."
]

def generate_poems(num_poems):
    poems = []
    for _ in range(num_poems):
        # Генерируем стихотворение из 4-х строк
        poem = random.sample(lines, 4)  
        poems.append("\n".join(poem))  # Объединяем строки в стих
    return poems

while True:
    num = int(input("Введите количество генерируемых стихов (0 для выхода): "))
    
    if num == 0:
        print("Выход из программы.")
        break

    if num < 0:
        print("Введите положительное число или 0 для выхода.")
        continue
    
    generated_poems = generate_poems(num)

    # Выводим сгенерированные стихотворения
    for index, poem in enumerate(generated_poems):
        print(f"\nСтихотворение {index + 1}:\n{poem}\n")
