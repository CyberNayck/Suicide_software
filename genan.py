    "Мужчина заходит в аптеку и спрашивает: 'У вас есть что-то от усталости?' Аптекарь отвечает: 'Конечно, вот, например, отпуск!'",
    "Приходит мужик в аптеку. У него такой вид, будто он только что вернулся с фронта. Он спрашивает: 'У вас есть что-то от стресса?' Аптекарь отвечает: 'Как насчет хорошего анекдота?' Мужик отвечает: 'Неплохо, но мне нужно что-то более серьезное!'",
    "Встречаются две подруги: 'Ты не представляешь, мой муж стал таким легкомысленным!' — 'Почему?' — 'Он сейчас весит 10 кг меньше, чем до свадьбы!'",
    "Звонит муж жене: 'Где ты?' Она: 'У подруги'. Он: 'Какого черта?' Она: 'А ты не знал, что у меня есть подруга?'",
    "Приходит ученик в школу и спрашивает: 'Учитель, за что вы меня выгнали?' Учитель: 'За то, что ты слишком часто задавал вопросы!'",
